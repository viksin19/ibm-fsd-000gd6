package com.example.task.data;

public class ErrorClass {
	private String message;

	public ErrorClass() {
		super();
	}

	public ErrorClass(String message) {
		super();
		this.message = message;
	}

	public String getMessage() {
		return message;
	}

	public void setMessage(String message) {
		this.message = message;
	}
	
}
